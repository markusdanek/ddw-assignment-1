var express = require('express');
var mongoose = require('mongoose');
var user = mongoose.model('User');
var router = express.Router();

router.get('/users', function(req, res) {
  user.find(function(err, User){
    console.log(User);
    res.render(
      'users',
      {title : 'User', User : User}
    );
  });
});
